import type { LetterContent } from '@/shared/types/letter'

type LetterTemplate = {
  type: LetterContent['template']
  build: (domain: string) => Omit<LetterContent, 'template'>
}

// Не используем в письмах спам-триггеры: финансовые обещания
// (скидка, бесплатно, заработок, выгода), давление срочностью
// (срочно, только сегодня, сейчас), кликовые команды (нажмите,
// кликните), абсолютные гарантии (100%, гарантируем, без риска),
// агрессивные превосходные степени, капс и лишние восклицания.
const SENTRY_MIGRATION_TEMPLATE: LetterTemplate = {
  type: 'sentry_migration',
  build: (domain) => ({
    subject: `Hawk для ${domain}: миграция с Sentry`,
    body: [
      'Здравствуйте!',
      '',
      `На сайте ${domain} мы заметили Sentry. Hawk — российский трекер ошибок с открытым исходным кодом и серверами в РФ. Он поддерживает привычный формат Sentry SDK, поэтому переход не требует переписывать уже встроенные обработчики.`,
      '',
      'Главное в миграции: обычно достаточно заменить одну строку в конфиге — значение dsn. SDK, точки инициализации и основной код приложения можно оставить прежними.',
      '',
      'После подключения команда видит ошибки, трассировку, параметры запроса, контекст пользователя, релизы и ответственных в одном интерфейсе. Это помогает понять, какой участок кода привел к проблеме, и спокойнее сопровождать изменения в продукте.',
      '',
      'Если для вас актуальна российская инфраструктура мониторинга ошибок - hawk-tracker .ru',
      '',
      'С уважением,',
      'Команда Hawk',
    ].join('\n'),
  }),
}

const STANDARD_OFFER_TEMPLATE: LetterTemplate = {
  type: 'standard_offer',
  build: (domain) => ({
    subject: `Hawk для ${domain}: мониторинг ошибок`,
    body: [
      'Здравствуйте!',
      '',
      `Мы посмотрели сайт ${domain}. Hawk — российский трекер ошибок для веб-приложений: собирает ошибки, показывает трассировку, параметры запроса, контекст пользователя и связь с релизами.`,
      '',
      'Подключение занимает минимум изменений в коде. Если у вас уже используется Sentry, переход обычно сводится к одной строке в конфиге — нужно заменить значение dsn. Для новых подключений есть SDK для JavaScript/TypeScript, React, Vue, Nuxt, Node.js, PHP, Python, Go и других стеков.',
      '',
      'Хоук размещает сервера в России, имеет открытый исходный код и помогает команде видеть проблемные места без сложной настройки.',
      '',
      'Если для вас актуальна российская инфраструктура мониторинга ошибок - hawk-tracker .ru',
      '',
      'С уважением,',
      'Команда Hawk',
    ].join('\n'),
  }),
}

export function buildLetterContent(
  domain: string,
  sentry: boolean,
): LetterContent {
  const template = sentry ? SENTRY_MIGRATION_TEMPLATE : STANDARD_OFFER_TEMPLATE
  const content = template.build(domain)

  return {
    template: template.type,
    ...content,
  }
}
